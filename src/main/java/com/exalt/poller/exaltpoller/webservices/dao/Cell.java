package com.exalt.poller.exaltpoller.webservices.dao;

public class Cell {

	private String f;
	private String v;
	
	public Cell() {
		
	}

	public Cell(String f, String v) {
		super();
		this.f = f;
		this.v = v;
	}

	public String getF() {
		return f;
	}

	public void setF(String f) {
		this.f = f;
	}

	public String getV() {
		return v;
	}

	public void setV(String v) {
		this.v = v;
	}
}
